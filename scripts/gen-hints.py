def framing_hints(topic_id: str, section: str) -> list[str]:
    """Reuse fix-hints' topic/section framing hints as a fallback base."""
    return _fix.find_hints(topic_id, section) or _fix.find_hints(topic_id, None) or []


def compose(problem: dict) -> list[str]:
    # 0. hand-written per-problem override wins outright
    override = PROBLEM_HINTS.get(problem.get("id", ""))
    if override:
        return override[:MAX_HINTS]

    tags = problem.get("tags", [])
    out: list[str] = []
    seen: set[str] = set()

    def add(h: str):
        if h and h not in seen:
            seen.add(h)
            out.append(h)

    # 1. specific technique hints, in the tag order curated by 0x3f
    for t in tags:
        if is_generic(t) or t in SOFT:
            continue
        if t in TECH:
            add(TECH[t])
        if len(out) >= MAX_HINTS:
            break

    # 2. soft-generic tags only if we still need more
    if len(out) < MAX_HINTS:
        for t in tags:
            if t in SOFT and t in TECH:
                add(TECH[t])
            if len(out) >= MAX_HINTS:
                break

    # 3. top up with section-level then topic/section framing
    topic = problem.get("topic_id", "")
    refs = problem.get("study_plan_refs", [])
    section = refs[0].get("section_title", "").lstrip(". ").strip() if refs else ""
    if len(out) < MIN_HINTS and section:
        for (kt, ks), hs in SECTION_HINTS.items():
            if kt == topic and ks in section:
                for h in hs:
                    add(h)
                break
    if len(out) < MIN_HINTS:
        for h in framing_hints(topic, section):
            add(h)
            if len(out) >= MAX_HINTS:
                break

    # 4. last resort: guaranteed topic-level hints so no problem is ever empty
    if len(out) < MIN_HINTS:
        for h in TOPIC_FALLBACK.get(topic, []):
            add(h)
            if len(out) >= MIN_HINTS:
                break

    return out[:MAX_HINTS]


def main():
    path = os.path.join(ROOT, "data", "problems.json")
    with open(path, encoding="utf-8") as f:
        problems = json.load(f)

    stats = {"specific": 0, "framing_only": 0, "short": 0}
    for p in problems:
        new = compose(p)
        p["strategy_hints"] = new
        spec = [t for t in p.get("tags", []) if not is_generic(t) and t in TECH]
        if spec:
            stats["specific"] += 1
        else:
            stats["framing_only"] += 1
        if len(new) < MIN_HINTS:
            stats["short"] += 1

    with open(path, "w", encoding="utf-8") as f:
        json.dump(problems, f, ensure_ascii=False, indent=2)

    print(f"Updated {len(problems)} problems.")
    print(f"  with >=1 technique-specific hint : {stats['specific']}")
    print(f"  framing-only (generic tags)      : {stats['framing_only']}")
    print(f"  fewer than {MIN_HINTS} hints (check)        : {stats['short']}")


if __name__ == "__main__":
    main()
