"""
Compose per-problem strategy hints.

Problem with the old approach (scripts/fix-hints.py): hints were keyed only on
(topic_id, section_title), so ~3,500 problems shared just 151 generic hint
sets. This script instead builds each problem's hints from *its own technique
tags*, so two problems with different techniques get different hints, and every
hint actually describes a technique the problem uses.

Composition per problem:
  1. Pull a concrete hint for each of the problem's specific tags (TECH map),
     in tag order, de-duplicated. These are the problem-specific hints.
  2. If that yields fewer than MIN_HINTS, top up with the topic/section
     "framing" hints (reused from fix-hints.HINTS) so the problem still gets a
     coherent set describing what kind of problem it is.
  3. Cap at MAX_HINTS.

GENERIC tags (陣列/字串/動態規劃/競賽題庫/...) carry no technique signal, so they
are skipped in step 1 and only feed the framing fallback. No problem is left
without hints.
"""
